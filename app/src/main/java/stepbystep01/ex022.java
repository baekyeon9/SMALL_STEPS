package stepbystep01;

public class ex022 {

  public static void main(String[] args) {

    int i = 3; 
    i++;
    System.out.println(i);

    ++i;
    System.out.println(i);

    System.out.println(++i); // 증가시키고 사용

    System.out.println(i++); // 사용하고 증가시킨다 i = 6;

    System.out.println(i);
  }

}
